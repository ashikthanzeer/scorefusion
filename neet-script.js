function inferNeetSubject(questionNo) {
  const range = SUBJECT_RANGES.find((range) => questionNo >= range.start && questionNo <= range.end);
  return range?.name || 'Unknown';
}

function isDroppedNeetQuestion(correctAnswerId) {
  return String(correctAnswerId || '').toUpperCase() === 'DROP';
}

function getNeetCorrectAnswerIds(correctAnswerId) {
  return String(correctAnswerId || '')
    .split(',')
    .map((answerId) => answerId.trim())
    .filter(Boolean);
}

function calculateNeetResult(responses, answerKey) {
  const subjectStats = createEmptyNeetSubjectStats();

  const details = [];
  const responseMap = new Map(responses.map((item) => [item.questionId, item]));
  const questionIds = Array.from(new Set([...Object.keys(answerKey), ...responses.map((item) => item.questionId)])).sort();

  questionIds.forEach((questionId) => {
    const answerMeta = answerKey[questionId];
    const subject = answerMeta?.subject || inferNeetSubject(Number(questionId));
    const correctAnswerId = answerMeta?.correctAnswerId || null;
    const hasKey = Boolean(correctAnswerId && correctAnswerId !== '-');
    const dropped = isDroppedNeetQuestion(correctAnswerId);
    const response = responseMap.get(questionId);
    const attempted = Boolean(response?.attempted);
    const chosenOptionId = response?.chosenOptionId || null;

    let correct = false;
    if (dropped) {
      correct = true;
    } else if (attempted && hasKey && chosenOptionId) {
      correct = getNeetCorrectAnswerIds(correctAnswerId).includes(chosenOptionId);
    }

    if (dropped) {
      subjectStats[subject].c += 1;
      subjectStats[subject].s += MARKS_CORRECT;
    } else if (!attempted) {
      subjectStats[subject].u += 1;
    } else if (hasKey && correct) {
      subjectStats[subject].c += 1;
      subjectStats[subject].s += MARKS_CORRECT;
    } else if (hasKey && attempted) {
      subjectStats[subject].w += 1;
      subjectStats[subject].s += MARKS_WRONG;
    }

    const result = dropped
      ? 'Dropped'
      : !attempted
      ? 'Skipped'
      : hasKey
        ? (correct ? 'Correct' : 'Wrong')
        : 'Unverified';

    details.push({
      questionNo: Number(questionId),
      questionId,
      subject,
      attempted,
      correct,
      chosenOptionId,
      correctAnswerId: hasKey ? correctAnswerId : null,
      result
    });
  });

  const totalQuestions = details.length;
  const totalMarks = totalQuestions * MARKS_CORRECT;
  const totalCorrect = Object.values(subjectStats).reduce((sum, val) => sum + val.c, 0);
  const totalWrong = Object.values(subjectStats).reduce((sum, val) => sum + val.w, 0);
  const totalSkipped = Object.values(subjectStats).reduce((sum, val) => sum + val.u, 0);
  const totalScore = Object.values(subjectStats).reduce((sum, val) => sum + val.s, 0);
  const totalAttempted = totalCorrect + totalWrong;
  const overallAccuracy = totalAttempted ? Math.round((totalCorrect / totalAttempted) * 100) : 0;
  const attemptRate = totalQuestions ? Math.round((totalAttempted / totalQuestions) * 100) : 0;
  const percentage = totalMarks ? Math.round((totalScore / totalMarks) * 100) : 0;

  return {
    scores: subjectStats,
    details,
    totalQuestions,
    totalScore,
    totalMarks,
    totalCorrect,
    totalWrong,
    totalSkipped,
    overallAccuracy,
    attemptRate,
    percentage
  };
}

function createEmptyNeetSubjectStats() {
  return NEET_SUBJECTS.reduce((stats, subject) => {
    stats[subject] = { c: 0, w: 0, u: 0, s: 0 };
    return stats;
  }, {});
}

function getNeetSubjectStats(scores, subject) {
  const entry = scores?.[subject];
  return {
    c: Number(entry?.c) || 0,
    w: Number(entry?.w) || 0,
    u: Number(entry?.u) || 0,
    s: Number(entry?.s) || 0
  };
}

function isRestorableNeetResult(result) {
  return Boolean(
    result &&
    result.scores &&
    Array.isArray(result.details) &&
    NEET_SUBJECTS.every((subject) => result.scores[subject])
  );
}

window.addEventListener('DOMContentLoaded', () => {
  const htmlFile = document.getElementById('htmlFile');
  const dropZone = document.getElementById('dropZone');
  const dropText = document.getElementById('dropText');

  document.getElementById('analyzeBtn')?.addEventListener('click', analyzeNeet);
  document.getElementById('themeToggle')?.addEventListener('click', toggleTheme);
  document.getElementById('exportPdfBtn')?.addEventListener('click', exportNeetPdf);
  document.getElementById('clearDataBtn')?.addEventListener('click', clearNeetData);

  htmlFile.addEventListener('change', async () => {
    if (htmlFile.files?.[0]) {
      const file = htmlFile.files[0];
      dropText.textContent = '📎 ' + file.name;
      dropText.style.color = 'var(--success)';
      try {
        const shiftId = detectNeetBookletCode(await file.text());
        if (shiftId) {
          document.getElementById('shift').value = shiftId;
          const label = document.querySelector(`#shift option[value="${shiftId}"]`)?.textContent || shiftId;
          dropText.textContent = `📎 ${file.name} - ${label}`;
        }
      } catch (error) {
        console.warn('Could not detect NEET booklet code:', error);
      }
    } else {
      dropText.textContent = 'Tap to choose or drag & drop HTML';
      dropText.style.color = '';
    }
  });

  ['dragenter', 'dragover'].forEach((evt) => {
    dropZone.addEventListener(evt, (e) => { e.preventDefault(); dropZone.classList.add('dragover'); });
  });
  dropZone.addEventListener('dragleave', () => dropZone.classList.remove('dragover'));
  dropZone.addEventListener('drop', (e) => {
    e.preventDefault();
    dropZone.classList.remove('dragover');
    if (e.dataTransfer.files?.[0]) {
      htmlFile.files = e.dataTransfer.files;
      dropText.textContent = '📎 ' + e.dataTransfer.files[0].name;
      dropText.style.color = 'var(--success)';
    }
  });

  applyTheme(localStorage.getItem('theme') || 'light');
  restoreNeetData();
});

function applyTheme(theme) {
  document.body.classList.toggle('dark', theme === 'dark');
  localStorage.setItem('theme', theme);
  const btn = document.getElementById('themeToggle');
  if (btn) btn.innerText = theme === 'dark' ? '☀️' : '🌙';
  const themeMeta = document.getElementById('themeColorMeta');
  if (themeMeta) themeMeta.setAttribute('content', theme === 'dark' ? '#0f1117' : '#f4f6fb');
  if (_lastResult) drawRing(_lastResult);
}

function toggleTheme() {
  applyTheme(document.body.classList.contains('dark') ? 'light' : 'dark');
}

function setAnalyzing(isAnalyzing) {
  const btn = document.getElementById('analyzeBtn');
  if (!btn) return;
  btn.disabled = isAnalyzing;
  document.getElementById('analyzeBtnText').textContent = isAnalyzing ? 'Analyzing…' : 'Analyze';
  document.getElementById('analyzeBtnSpinner').hidden = !isAnalyzing;
}

async function analyzeNeet() {
  let shiftId = document.getElementById('shift').value;
  const file = document.getElementById('htmlFile').files[0];
  if (!file) {
    return alert('Please select an exam code and upload the NEET HTML response sheet.');
  }

  setAnalyzing(true);
  try {
    const htmlText = await file.text();
    if (!shiftId) {
      shiftId = detectNeetBookletCode(htmlText);
      if (shiftId) document.getElementById('shift').value = shiftId;
    }
    if (!shiftId) {
      alert('Please select an exam code. I could not auto-detect it from this response sheet.');
      return;
    }

    const answerKey = ANSWER_KEYS[shiftId];
    if (!answerKey) return alert('Answer key missing for this exam code.');

    const responses = parseNeetHtml(htmlText);
    if (!responses.length) {
      throw new Error('No NEET question rows were found in the uploaded HTML file.');
    }
    const result = calculateNeetResult(responses, answerKey);
    result.shiftId = shiftId;
    result.shiftLabel = document.querySelector(`#shift option[value="${shiftId}"]`)?.textContent || shiftId;
    result.sourceFileName = file.name;
    result.generatedAt = new Date().toISOString();
    _lastResult = result;
    renderNeetResult(result);
    localStorage.setItem(STORAGE_KEY, JSON.stringify(result));
  } catch (error) {
    console.error('NEET analysis failed:', error);
    alert('Failed to analyze the NEET response HTML. Please check the file and try again.');
  } finally {
    setAnalyzing(false);
  }
}

function renderNeetResult(result) {
  const scores = result.scores || {};
  const tbody = document.getElementById('tbody');
  tbody.innerHTML = '';

  let totalScore = 0;
  let totalCorrect = 0;
  let totalWrong = 0;
  let totalSkipped = 0;

  NEET_SUBJECTS.forEach((subject) => {
    const entry = getNeetSubjectStats(scores, subject);
    const attempted = entry.c + entry.w;
    const acc = attempted ? Math.round((entry.c / attempted) * 100) : 0;
    totalScore += entry.s;
    totalCorrect += entry.c;
    totalWrong += entry.w;
    totalSkipped += entry.u;
    tbody.innerHTML += `<tr>
      <td>${subject}</td>
      <td class="score-col">${entry.s}</td>
      <td class="correct-col">${entry.c}</td>
      <td class="wrong-col">${entry.w}</td>
      <td class="skip-col">${entry.u}</td>
      <td>${acc}%</td>
    </tr>`;
  });

  const totalAttempted = totalCorrect + totalWrong;
  const overallAccuracy = totalAttempted ? Math.round((totalCorrect / totalAttempted) * 100) : 0;
  tbody.innerHTML += `<tr style="font-weight:bold;">
    <td><strong>Total</strong></td><td><strong>${totalScore}</strong></td><td><strong>${totalCorrect}</strong></td>
    <td><strong>${totalWrong}</strong></td><td><strong>${totalSkipped}</strong></td><td><strong>${overallAccuracy}%</strong></td>
  </tr>`;

  document.getElementById('resultsSection').style.display = 'block';
  document.getElementById('charts').style.display = 'block';
  document.getElementById('questionAnalysis').style.display = 'block';

  drawRing(result);
  drawChart(result);
  renderQuestionAnalysis(result.details || []);
}

function renderQuestionAnalysis(details) {
  const tbody = document.querySelector('#detailTable tbody');
  tbody.innerHTML = '';
  details.forEach((d, index) => {
    const answered = d.attempted ? d.chosenOptionId : '-';
    const tr = document.createElement('tr');
    const cls = d.correct ? 'result-correct' : (d.attempted ? 'result-wrong' : 'result-skip');
    tr.innerHTML = `
      <td>${index + 1}</td>
      <td>${d.questionId}</td>
      <td>${answered}</td>
      <td>${d.correctAnswerId || '-'}</td>
      <td class="${cls}">${d.result}</td>
    `;
    tbody.appendChild(tr);
  });
}

function exportNeetPdf() {
  if (!_lastResult) return alert('Analyze a response sheet first.');
  try {
    const normalized = _lastResult;
    const pdf = window.PdfExportUtils.createDoc();
    window.PdfExportUtils.addTitle(pdf, 'NEET UG Score Analysis - ScoreFusion', [normalized.shiftLabel]);
    window.PdfExportUtils.addSection(pdf, 'Summary');
    window.PdfExportUtils.addKeyValueLines(pdf, [
      { label: 'Total Score', value: `${normalized.totalScore} / ${normalized.totalMarks}` },
      { label: 'Overall Accuracy', value: `${normalized.overallAccuracy}%` },
      { label: 'Attempt Rate', value: `${normalized.attemptRate}%` },
      { label: 'Percentage', value: `${normalized.percentage}%` }
    ]);

    const rows = NEET_SUBJECTS.map((subject) => {
      const entry = getNeetSubjectStats(normalized.scores, subject);
      const attempted = entry.c + entry.w;
      const acc = attempted ? Math.round((entry.c / attempted) * 100) : 0;
      return [subject, String(entry.s), String(entry.c), String(entry.w), String(entry.u), `${acc}%`];
    });
    rows.push(['Total', String(normalized.totalScore), String(normalized.totalCorrect), String(normalized.totalWrong), String(normalized.totalSkipped), `${normalized.overallAccuracy}%`]);
    window.PdfExportUtils.addSection(pdf, 'Subject-wise Score Analysis');
    window.PdfExportUtils.addTable(pdf, [
      { label: 'Subject', width: 0.25 },
      { label: 'Score', width: 0.15 },
      { label: 'Correct', width: 0.15 },
      { label: 'Wrong', width: 0.15 },
      { label: 'Skipped', width: 0.15 },
      { label: 'Acc.', width: 0.15 }
    ], rows, { boldLastRow: true });
    window.PdfExportUtils.save(pdf, `neet-score-analysis-${normalized.shiftId}.pdf`);
  } catch (error) {
    console.error('NEET PDF export failed:', error);
    alert('Unable to export PDF right now.');
  }
}

function drawRing(result) {
  const total = result.totalScore;
  const max = result.totalMarks;
  const pct = max ? (total / max) * 100 : 0;
  const attempted = result.totalCorrect + result.totalWrong;
  const accPct = attempted ? (result.totalCorrect / attempted) * 100 : 0;
  const isDark = document.body.classList.contains('dark');
  const trackColor = isDark ? '#2e3347' : '#dde3ee';
  const themeColor = isDark ? '#e2e8f0' : '#1a1a2e';

  function setup(canvas) {
    const dpr = window.devicePixelRatio || 1;
    const width = canvas.parentElement.clientWidth || 200;
    const height = Math.round(width * 0.55);
    canvas.style.width = `${width}px`;
    canvas.style.height = `${height}px`;
    canvas.width = width * dpr;
    canvas.height = height * dpr;
    const ctx = canvas.getContext('2d');
    ctx.scale(dpr, dpr);
    return { ctx, width, height };
  }

  function render(canvas, value, suffix, color, label) {
    const { ctx, width, height } = setup(canvas);
    const radius = width / 2 - Math.max(10, width * 0.07);
    const lineWidth = Math.max(8, width * 0.06);
    ctx.clearRect(0, 0, width, height);
    ctx.lineWidth = lineWidth;
    ctx.beginPath(); ctx.arc(width / 2, height, radius, Math.PI, 0); ctx.strokeStyle = trackColor; ctx.stroke();
    ctx.beginPath(); ctx.arc(width / 2, height, radius, Math.PI, Math.PI + Math.PI * (value / 100)); ctx.strokeStyle = color; ctx.lineCap = 'round'; ctx.stroke();
    ctx.font = `${Math.max(10, width * 0.065)}px system-ui`; ctx.fillStyle = themeColor; ctx.textAlign = 'center';
    ctx.fillText(label, width / 2, height - radius / 1.5);
    ctx.font = `bold ${Math.max(16, width * 0.12)}px system-ui`;
    ctx.fillText(`${Math.round(value)}${suffix}`, width / 2, height - radius / 3.2);
  }

  render(document.getElementById('scoreRing'), pct, '%', '#3b82f6', 'Total marks:');
  render(document.getElementById('accuracyRing'), accPct, '%', '#10b981', 'Accuracy:');
}

function drawChart(result) {
  const ctx = document.getElementById('scoreChart').getContext('2d');
  if (window.scoreChartInstance) window.scoreChartInstance.destroy();
  window.scoreChartInstance = new Chart(ctx, {
    type: 'pie',
    data: {
      labels: NEET_SUBJECTS,
      datasets: [{
        data: NEET_SUBJECTS.map((subject) => getNeetSubjectStats(result.scores, subject).s),
        backgroundColor: ['#3b82f6', '#10b981', '#f59e0b', '#8b5cf6']
      }]
    },
    options: { responsive: true, maintainAspectRatio: false }
  });
}

function clearNeetData() {
  localStorage.removeItem(STORAGE_KEY);
  _lastResult = null;
  document.getElementById('resultsSection').style.display = 'none';
  document.getElementById('charts').style.display = 'none';
  document.getElementById('questionAnalysis').style.display = 'none';
  document.getElementById('tbody').innerHTML = '';
  document.querySelector('#detailTable tbody').innerHTML = '';
  document.getElementById('htmlFile').value = '';
  document.getElementById('dropText').textContent = 'Tap to choose or drag & drop HTML';
  document.getElementById('dropText').style.color = '';
}

function restoreNeetData() {
  try {
    const stored = JSON.parse(localStorage.getItem(STORAGE_KEY));
    if (stored) {
      if (!isRestorableNeetResult(stored)) {
        localStorage.removeItem(STORAGE_KEY);
        return;
      }
      _lastResult = stored;
      document.getElementById('shift').value = stored.shiftId || '';
      renderNeetResult(stored);
    }
  } catch (error) {
    console.warn('Failed restoring NEET data:', error);
  }
}
